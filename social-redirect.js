// Get platform from URL parameter or default to mastodon
const urlParams = new URLSearchParams(window.location.search);
const platformKey = urlParams.get('platform') || 'mastodon';
const platform = platforms[platformKey];

// Update page with platform info
document.addEventListener('DOMContentLoaded', () => {
    const platformIcon = document.querySelector('.platform-icon');
    const platformName = document.querySelector('.platform-details h2');
    const platformHandle = document.querySelector('.handle');
    const platformDesc = document.querySelector('.description');
    const redirectText = document.querySelector('.redirect-text');
    const goNowBtn = document.getElementById('goNowBtn');
    
    // Update content
    platformIcon.className = `platform-icon ${platformKey}`;
    platformIcon.innerHTML = getPlatformIcon(platformKey);
    platformName.textContent = platform.name;
    platformHandle.textContent = platform.handle;
    platformDesc.textContent = platform.description;
    redirectText.textContent = `Redirecting to ${platform.name}...`;
    goNowBtn.href = platform.url;
    
    // Start countdown
    let count = 5;
    const countdownEl = document.getElementById('countdown');
    
    function updateCountdown() {
        if (count > 0) {
            countdownEl.textContent = count;
            count--;
        } else {
            window.location.href = platform.url;
        }
    }
    
    const countdownInterval = setInterval(updateCountdown, 1000);
    
    // Allow manual redirect
    goNowBtn.addEventListener('click', (e) => {
        e.preventDefault();
        clearInterval(countdownInterval);
        window.location.href = platform.url;
    });
    
    // Cancel redirect
    document.querySelector('.learn-more-btn').addEventListener('click', (e) => {
        e.preventDefault();
        clearInterval(countdownInterval);
        window.location.href = 'index.html';
    });
});

